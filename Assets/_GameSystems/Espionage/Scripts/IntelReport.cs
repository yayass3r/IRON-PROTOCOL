        /// <summary>
        /// Generates a report based on the available intel level, choosing the most
        /// informative category automatically.
        /// </summary>
        private IntelReport GenerateReportFromIntel(string sourceNation, string targetNation, NationIntel intel)
        {
            if (UnityEngine.Random.value > networkReportChance)
                return null;

            // Determine the strongest intel category to report on
            IntelLevel maxIntel = IntelLevel.None;
            IntelReportCategory bestCategory = IntelReportCategory.General;

            if (intel.MilitaryIntel > maxIntel)
            {
                maxIntel = intel.MilitaryIntel;
                bestCategory = IntelReportCategory.Military;
            }
            if (intel.EconomicIntel > maxIntel)
            {
                maxIntel = intel.EconomicIntel;
                bestCategory = IntelReportCategory.Economic;
            }
            if (intel.TechIntel > maxIntel)
            {
                maxIntel = intel.TechIntel;
                bestCategory = IntelReportCategory.Technology;
            }
            if (intel.DiplomaticIntel > maxIntel)
            {
                maxIntel = intel.DiplomaticIntel;
                bestCategory = IntelReportCategory.Diplomatic;
            }

            if (maxIntel == IntelLevel.None)
                return null;

            IntelReport report = null;

            // Add some randomness to category selection
            float categoryRoll = UnityEngine.Random.value;
            if (categoryRoll < 0.35f || bestCategory == IntelReportCategory.Military)
                report = GenerateMilitaryReport(targetNation, _hexGrid);
            else if (categoryRoll < 0.60f || bestCategory == IntelReportCategory.Economic)
                report = GenerateEconomicReport(targetNation);
            else if (categoryRoll < 0.80f || bestCategory == IntelReportCategory.Technology)
                report = GenerateTechReport(targetNation);
            else
                report = GenerateDiplomaticReport(targetNation);

            if (report != null)
            {
                report.SourceNation = sourceNation;
            }

            return report;
        }

        /// <summary>
        /// Stores a report and indexes it by receiving nation.
        /// Enforces maximum report count by archiving oldest reports.
        /// </summary>
        private void StoreReport(IntelReport report, string receivingNation)
        {
            _reports[report.ReportId] = report;

            if (!_reportsByNation.ContainsKey(receivingNation))
                _reportsByNation[receivingNation] = new List<string>();
            _reportsByNation[receivingNation].Add(report.ReportId);

            Debug.Log($"[IntelReportManager] New report: [{report.Category}] {report.Title} " +
                       $"(Confidence: {report.Confidence})");
            OnNewReportGenerated?.Invoke(report, receivingNation);

            // Enforce max report limit
            EnforceReportLimit();
        }

        /// <summary>
        /// Removes the oldest reports when the total count exceeds <see cref="maxStoredReports"/>.
        /// </summary>
        private void EnforceReportLimit()
        {
            if (_reports.Count <= maxStoredReports)
                return;

            var oldestReports = _reports.Values
                .OrderBy(r => r.TurnNumber)
                .Take(_reports.Count - maxStoredReports)
                .ToList();

            foreach (IntelReport old in oldestReports)
            {
                _reports.Remove(old.ReportId);
                foreach (var kvp in _reportsByNation)
                {
                    kvp.Value.Remove(old.ReportId);
                }
            }

            Debug.Log($"[IntelReportManager] Archived {oldestReports.Count} old reports (limit: {maxStoredReports}).");
        }

        /// <summary>
        /// Returns a random economic health descriptor for low-intel reports.
        /// </summary>
        private string GetRandomEconomicHealth()
        {
            string[] states = { "Struggling", "Stable", "Growing", "Declining", "Recovering" };
            return states[UnityEngine.Random.Range(0, states.Length)];
        }

        /// <summary>
        /// Returns a random technological era descriptor for low-intel reports.
        /// </summary>
        private string GetRandomTechEra()
        {
            string[] eras = { "Pre-Industrial", "Early Industrial", "Industrial", "Post-Industrial", "Advanced" };
            return eras[UnityEngine.Random.Range(0, eras.Length)];
        }

        /// <summary>
        /// Estimates the current turn number from the espionage manager's state.
        /// Falls back to a counter-based estimate if no manager is available.
        /// </summary>
        private int GetEstimatedTurnNumber()
        {
            // Use the report counter as a rough turn estimate when no turn manager is linked
            return _reportIdCounter > 0 ? (_reportIdCounter / 3) + 1 : 1;
        }

        /// <summary>
        /// Searches the scene for the EspionageManager instance.
        /// </summary>
        private EspionageManager FindEspionageManager()
        {
            // Try FindObjectOfType first (works if already in scene)
            var manager = FindAnyObjectByType<EspionageManager>();
            if (manager != null)
                return manager;

            // Fallback: search for GameManager and get component
            var gameManager = GameObject.Find("GameManager");
            return gameManager != null ? gameManager.GetComponent<EspionageManager>() : null;
        }

        /// <summary>
        /// Searches the scene for the HexGrid instance.
        /// </summary>
        private HexGrid FindHexGrid()
        {
            var grid = FindAnyObjectByType<HexGrid>();
            if (grid != null)
                return grid;

            var gridObj = GameObject.Find("HexGrid");
            return gridObj != null ? gridObj.GetComponent<HexGrid>() : null;
        }
